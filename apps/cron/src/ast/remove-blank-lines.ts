/**
 * 空行（空白文字のみの行を含む）をすべて取り除いて行を詰める。
 *
 * コメント除去後に残るインデントだけの行（`  \n`）や、元から本文に
 * 含まれていた空行はタイピング中のノイズになるだけなので、問題文には
 * 一切残さない方針。`^[^\S\n]*\n` で「改行以外の空白だけ + 改行」の行を
 * 行末の改行ごと丸ごと削除する（`\r\n` 改行も `[^\S\n]` が `\r` を拾う）。
 * 内容のある行の行末改行は対象外なので、末尾の改行は保持される。
 */
export const removeBlankLines = (text: string): string => text.replace(/^[^\S\n]*\n/gm, "")
